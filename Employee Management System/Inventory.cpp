#include "Inventory.h"
void Inv::setItemNumber(int itmn)
{

	itemNo = itmn;
};
/*************************************************************************************************/
void Inv::setQuantity(int qnt)
{

	quantity = qnt;
};
/**************************************************************************************************/
void Inv::setCost(double c)
{

	cost = c;
};
/*************************************************************************************************/
void Inv::setTotalCost()
{

	totalCost = cost * quantity;
};
/*************************************************************************************************/
int Inv::getItemNumber()
{

	return itemNo;
};
/*************************************************************************************************/
int Inv::getQuantity()
{

	return quantity;
};
/**************************************************************************************************/
double Inv::getCost()
{

	return cost;
};
/**************************************************************************************************/
double Inv::getTotalCost()
{

	return totalCost;
};
/***************************************************************************************************/
void Inv::Display()
{

	cout << "Item No :       " << getItemNumber();

	cout << endl;

	cout << "Quantity :      " << getQuantity();

	cout << endl;

	cout << "Cost per unit : " << getCost();

	cout << endl;

	cout << "Total cost :    " << getTotalCost();

	cout << endl;
};
